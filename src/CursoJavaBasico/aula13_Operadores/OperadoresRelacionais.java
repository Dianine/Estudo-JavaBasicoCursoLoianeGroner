package CursoJavaBasico.aula13_Operadores;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int x1 = 1;
		int x2 = 2;
		
		
		System.out.println(" x1 == x2: " + (x1 == x2));
		System.out.println(" x1 != x2: " + (x1 != x2));
		System.out.println(" x1 > x2: " + (x1 > x2));
		System.out.println(" x1 >= x2: " + (x1 >= x2));
		System.out.println(" x1 < x2: " + (x1 < x2));
		System.out.println(" x1 <= x2: " + (x1 <= x2));
		
		

	}

}
